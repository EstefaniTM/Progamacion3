let dia = "miercoles";

switch (dia) {
    case "lunes":
        console.log("Laboral");
        break;
    case "martes":
        console.log("Martes de Navidad");
        break;
    case "miercoles":
        console.log("Miercoles Halloween");
        break;
    case "jueves":
        console.log("Laboral");
        break;
    case "viernes":
        console.log("Viernes santo");
        break;
    case "sabado":
        console.log("No laboral");
        break;
    default:
        console.log("No es un dia valido");
}